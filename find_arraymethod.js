// Array method - find()   - to find a particular value from the given array

fruits= ['apple','mango','orange']

//w.a.p to print mango if mango is present in the array

find= fruits.find(fruits=>fruits=='mango')
console.log(find);

// array method - includes()    - to find the paticular item and return will be true or false

c=fruits.includes('mango')
console.log(c);

// array method - indexOf()   -   to find the index number of the given array name

d=fruits.indexOf('orange')
console.log(d);